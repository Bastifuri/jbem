
package Model.Data;

public class Procesador {
    private int id_proce;
    private String modeloP;
    private int precioP;

    public int getId_proce() {
        return id_proce;
    }

    public void setId_proce(int id_proce) {
        this.id_proce = id_proce;
    }

    public String getModeloP() {
        return modeloP;
    }

    public void setModeloP(String modeloP) {
        this.modeloP = modeloP;
    }

    public int getPrecioP() {
        return precioP;
    }

    public void setPrecioP(int precioP) {
        this.precioP = precioP;
    }
}
